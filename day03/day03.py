# Día 03 - Operadores y expresiones

a = 10
b = 3

# Operadores aritméticos
print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)
print("División entera:", a // b)
print("Módulo:", a % b)
print("Potencia:", a ** b)

# Operadores de comparación
print(a > b)
print(a == b)
print(a != b)

# Operadores lógicos
edad = 20
tiene_permiso = True

print(edad >= 18 and tiene_permiso)
print(edad < 18 or tiene_permiso)
print(not tiene_permiso)

print("¿Es mayor de edad y tiene permiso?:", edad >= 18 and tiene_permiso)
print("¿Es menor de edad o tiene permiso?:", edad < 18 or tiene_permiso)
print("¿No tiene permiso?:", not tiene_permiso)
